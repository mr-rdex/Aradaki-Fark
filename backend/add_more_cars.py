async def add_cars():
    """Add more cars to database"""
    
    for car in new_cars:
        existing = await db.cars.find_one({"CarID": car["CarID"]})
        if not existing:
            await db.cars.insert_one(car)
            print(f"Added: {car['ArabaMarka']} {car['CarModel']}")
        else:
            print(f"Already exists: {car['ArabaMarka']} {car['CarModel']}")
    
    total = await db.cars.count_documents({})
    print(f"\nTotal cars in database: {total}")
    client.close()

